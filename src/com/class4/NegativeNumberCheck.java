package com.class4;

public class NegativeNumberCheck {

	public static void main(String[] args) {
		int a=7;
		if (a<0) {
			System.out.println("The number "+a+" is negative.");
		}else if (a==0) {
			System.out.println("The number "+a+" is equal to zero.");
		}else {
			System.out.println("The number "+a+" is positive.");
		}
	}

}
