package com.class2;

public class VariablesDeclaration {
	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable

		y = 12; // 2. assign value

		// declare 1 by 1
		int a;
		int b;
		int c;
		int d;
		int e;

		a = 1;
		b = 2;
		c = 3;
		d = 4;
		e = 5;

		// or declare all together
		int x, m, z, w, v;

		x = 12;
		m = 34;
		z = 567;
		w = 67;
		v = 78;

		a = 10;
		b = 20;

		int h = 67;
		int re = 87;
		String nameLast = "hello";

		System.out.println(x);
		// for mac cmd+_shift+f = formatting
		// Windows ctrl_shift+f = formatting
	}

}
