package com.class7;

public class Loops {
	public static void main(String[] args) {
		/* to eliminate repetition we use loops
		System.out.println("Good morning");
		
		/*Loops in Java
		while
		do while
		for 
		enhanced for loop (for each loop) collections or arrays
		
		boolean morning=true;
		if (morning) {
			System.out.println("Good morning");*/
		
		
	int a=1;
		while (a<10) {
			System.out.println("Good morning");
			a++;
			
		}
		int b=0;
		
		while (b<=5) {
			System.out.println("I am inside while loop");
			b++;
		}
		System.out.println("I am outside of while loop");
		
		int z=10;
		while (z<=20) {
			System.out.println(z);
			z++;
		}
		int q=1;
		
		while (q<=20) {
			System.out.print(q+" ");
			q++;
		}
		System.out.println("___");
		int e=10;
		
		while(e>=10 ) {
			System.out.println(e);
			e--;
		}
	}
	

}
