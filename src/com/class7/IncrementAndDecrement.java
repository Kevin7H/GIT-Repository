package com.class7;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		int a=299;
		int b=a++;
		
		System.out.println(a);
		System.out.println(b);
		
		int x=10;
		int y=10;
		
		System.out.println(x+" "+y);
		
		int z=10;
		int c=z--;
		
		System.out.println(z);
		System.out.println(c);
	}

}
