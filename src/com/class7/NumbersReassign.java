package com.class7;

public class NumbersReassign {
	public static void main(String[] args) {
		
	int a=10;
	System.out.println(a);

	a+=100;
	System.out.println(a);
	a-=67;
	System.out.println(a);

	
	int cakePiece=11;
	System.out.println(cakePiece);

	cakePiece/=4;
	System.out.println(cakePiece);

	int cakePiece1=25;
	System.out.println(cakePiece1);

	cakePiece1%=7;
	System.out.println(cakePiece1);

	}

}
