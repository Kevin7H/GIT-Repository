package com.class1;

public class DataTypes {

	public static void main(String[] args) {
	/** Primitive Datatypes*/
		// Numbers Family
		byte 	b=-128;
		short 	s=1234;
		int 	c=123456;
		long	t=1234567890;
		// Decimals Family; for float always add the f a the end.
		float	z=23.8f;
		double	k=12.80787970888799668787997973;
		// Characters Family (A-Z)
		char	g='c';
		// True or False Family
		boolean	l=true;
	/** Imprimitive Variables*/
		// In String you can put anything as long as its in the quotation marks ""
		String str="Hello$";
		System.out.println(b);
		System.out.println(s);
		System.out.println(c);
		System.out.println(t);
		System.out.println(z);
		System.out.println(k);
		System.out.println(g);
		System.out.println(l);
		System.out.println(str);
	}

}
