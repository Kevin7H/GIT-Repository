package com.class3;

public class TemperatureCheck {
	public static void main(String[] args) {
		int freeze = 32;
		int temp = 7;

		if (temp <= freeze) {
			System.out.println("Water will freeze with temperature " + temp + ".");
		} else 
			System.out.println("Water will NOT freeze with temperature " + temp + ".");
		}
	
}