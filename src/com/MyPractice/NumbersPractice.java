package com.MyPractice;

public class NumbersPractice {
	public static void main(String[] args) {
		int a, b, c, d;
		a = 123;
		b = 13245;
		c = 10;
		d = 20;

		int sum, sub, mul, div;

		sum = (c + d);
		sub = (d - b);
		mul = (a * b);
		div = (d / b);

		System.out.println("The number " + sum + " is the sum of " + a + " and " + b + ".\n" + div
				+ " is the division of " + d + " and " + b + "." + sub + mul);

	}

}
